package aula5;

public class operadorUnario {

	public static void main(String[] args) {
		int num=  1;
		System.out.println("Numero: "+ num);
		//somando 1
		num = num +1;
		System.out.println("valor num:  " + num);
		
	// somando 1
		num += 1;
		System.out.println("valor num: " + num);
		
		num++; // ele soma apenas o 1
		System.out.println("valor num: " + num);
		
		num = num -1;
		num -= 1;
		--num; // sub apenas o 1.
	
	}
}
